import { motion, useTransform, useScroll } from "framer-motion";
import { useRef } from "react";

const Carousel = () => {
  return (
<section className=" w-[99.999%] m-auto bg-[var(--main)] rounded-b-2xl">

          <div className=" m-auto pl-4" >
              <h1 className="md:text-7xl text-6xl text-white relative md:top-20 top-40 pl-5">Danceplace</h1>
          </div>
          <div className="hero-img-container">

          </div>


      <HorizontalScrollCarousel />

</section>

  );
};

const HorizontalScrollCarousel = () => {
  const targetRef = useRef(null);
  const { scrollYProgress } = useScroll({
    target: targetRef,
  });

  const x = useTransform(scrollYProgress, [0, 1], ["1%", "-95%"]);

  return (
    <section ref={targetRef} className="relative h-[300vh]">
      <div className="sticky top-0 flex h-screen w-full  items-center !overflow-x-hidden">
        <motion.div style={{ x }} className="flex gap-4">
          {cards.map((card) => {
            return <Card card={card} key={card.id} />;
          })}
        </motion.div>
      </div>
    </section>
  );
};

const Card = ({ card }) => {
  return (
    <div
      key={card.id}
      className="group rounded-[1rem] relative md:h-[450px] md:w-[450px] h-[400px] w-[300px] !overflow-hidden bg-white"
    >
      <div
        style={{
          backgroundImage: `url(${card.url})`,
          backgroundSize: "cover",
          backgroundPosition: "center",
        }}
        className="absolute inset-0 z-0 transition-transform duration-300 group-hover:scale-110"
      ></div>

    </div>
  );
};

